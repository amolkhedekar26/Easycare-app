package com.example.easycare.dataclasses;

public class Hospitals {
    private int id;
    private String h_name;
    private String h_address;
    private int h_contact;
    private String h_speciality;
    private String h_location;
    private String h_rating;
    private int h_noofdoctors;
    private String h_time;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getH_name() {
        return h_name;
    }

    public void setH_name(String h_name) {
        this.h_name = h_name;
    }

    public String getH_address() {
        return h_address;
    }

    public void setH_address(String h_address) {
        this.h_address = h_address;
    }

    public int getH_contact() {
        return h_contact;
    }

    public void setH_contact(int h_contact) {
        this.h_contact = h_contact;
    }

    public String getH_speciality() {
        return h_speciality;
    }

    public void setH_speciality(String h_speciality) {
        this.h_speciality = h_speciality;
    }

    public String getH_location() {
        return h_location;
    }

    public void setH_location(String h_location) {
        this.h_location = h_location;
    }

    public String getH_rating() {
        return h_rating;
    }

    public void setH_rating(String h_rating) {
        this.h_rating = h_rating;
    }

    public int getH_noofdoctors() {
        return h_noofdoctors;
    }

    public void setH_noofdoctors(int h_noofdoctors) {
        this.h_noofdoctors = h_noofdoctors;
    }

    public String getH_time() {
        return h_time;
    }

    public void setH_time(String h_time) {
        this.h_time = h_time;
    }


}
